###Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrado###

num = float(input('Escreva um Número: '))

dobro = num*2
triplo = num*3
r_quadrada = num**1/2

print('O Cliente Escolheu o número {}\nO seu dobro é {}\nO seu triplo {}\nE sua raiz quadrada é {}'.format(num, dobro, triplo, r_quadrada))

